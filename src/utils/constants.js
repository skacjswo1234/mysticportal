export const calculateLifePath = (birthdate) => {
    const digits = birthdate.replace(/[^0-9]/g, '').split('').map(Number);
    let sum = digits.reduce((a, b) => a + b, 0);
    while (sum > 9 && sum !== 11 && sum !== 22) {
        sum = String(sum).split('').map(Number).reduce((a, b) => a + b, 0);
    }
    return sum;
};

